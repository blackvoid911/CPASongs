def update_urdu_books():
    output_dir = r"C:\Users\WelCome\AndroidStudioProjects\CPASongs\app\src\main\assets\bible\urdu"

    for book_name, chapters in COMPREHENSIVE_URDU_VERSES.items():
        # Determine file name
        if book_name.startswith("1 "):
            file_name = "1" + book_name[2:].lower().replace(" ", "") + ".json"
        elif book_name.startswith("2 "):
            file_name = "2" + book_name[2:].lower().replace(" ", "") + ".json"
        else:
            file_name = book_name.lower().replace(" ", "") + ".json"

        file_path = os.path.join(output_dir, file_name)

        if not os.path.exists(file_path):
            print(f"File not found: {file_name}")
            continue

        # Read current file
        with open(file_path, "r", encoding="utf-8") as f:
            data = json.load(f)

        # Update chapters with comprehensive verses
        updated_chapters = []
        for chapter_data in data["chapters"]:
            ch_num = chapter_data["chapter"]
            if ch_num in chapters:
                updated_chapters.append({
                    "chapter": ch_num,
                    "verses": chapters[ch_num]
                })
            else:
                updated_chapters.append(chapter_data)

        data["chapters"] = updated_chapters

        # Write back
        with open(file_path, "w", encoding="utf-8") as f:
            json.dump(data, f, ensure_ascii=False, indent=2)

        print(f"✓ Updated {book_name}")

if __name__ == "__main__":
    update_urdu_books()

